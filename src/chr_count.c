#include "ft_printf.h"

int		chr_count(char *str, char chr)
{
	int i;
	int	ct;

	i = 0;
	ct = 0;
	while (str[i])
	{
		if (str[i] == chr)
			ct++;
		i++;
	}
	return (ct);
}
